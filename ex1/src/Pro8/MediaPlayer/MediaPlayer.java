package Pro8.MediaPlayer;

public class MediaPlayer {
    private String menu;
    private String playList;
    private String mainWindow;
    private String controlBar;
    private String favouriteList;

    public String getMenu() {
        return menu;
    }

    public void setMenu(String menu) {
        this.menu = menu;
    }

    public String getPlayList() {
        return playList;
    }

    public void setPlayList(String playList) {
        this.playList = playList;
    }

    public String getMainWindow() {
        return mainWindow;
    }

    public void setMainWindow(String mainWindow) {
        this.mainWindow = mainWindow;
    }

    public String getControlBar() {
        return controlBar;
    }

    public void setControlBar(String controlBar) {
        this.controlBar = controlBar;
    }

    public String getFavouriteList() {
        return favouriteList;
    }

    public void setFavouriteList(String favouriteList) {
        this.favouriteList = favouriteList;
    }
}
